            <p class="featureP">If the domain hasn't given it away, my name is indeed James Now&bull;land, and I am graphic designer - specialising in web design based out of Newcastle, Australia.</p>
            <p>
For lack of a better phrase, I'm a design geek. I am interested in things that most people don't consciously appreciate, such as the way a great online experience is crafted through the combination of beautiful interface, appropriate use of technology and smart wireframes. I love minimal designs and the process in removing the unnecessary elements.

</p>
            <p>
               While I'm a "designer" - I have a passion for digital marketing. There is something about the unique ability of being able to track and monitor everything, which gives an intriguing insight to human thoughts. I have a real appreciation for seeing the results of highly targeted marketing combined with a user friendly website. 
           </p>

