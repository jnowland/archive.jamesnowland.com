            <h3>How we buy</h3>
            <span class="textDate">02 <em>of</em> Apr, 2013 </span>


            <p>The buying process has changed over the last few years with the rise of the net and social media. Since working in advertising I&#8217;m now extremely analytical about my own buying process.</p>
<p>Recently I bought a new road bike and kept a list of the steps I took.</p>
<h3>This is the way I buy things over and around $250:</h3>
<ol>
<li>Initial <a title="Road Bikes on eBay" href="http://www.ebay.com.au/sch/i.html?_trksid=p5197.m570.l1313&_nkw=road+bikes&_sacat=0&_from=R40" target="_blank">research on eBay</a> to see what is in my price range, discover a benchmark price to see how much local shops have marked up the product and weigh up the new vs second hand possibility.</li>
<li>Use this research I worked out my goals and what I wanted out of the product I&#8217;m researching.</li>
<li>Visit the sites of <a title="Felt" href="http://www.feltbicycles.com/Australia.aspx" target="_blank">the</a> <a title="Giant" href="http://www.giant-bicycles.com/en-au/" target="_blank">brands</a> <a title="Specialized" href="http://www.specialized.com/au/en-au/home" target="_blank">of</a> in the category I&#8217;m looking at.</li>
<li>Googling the product name plus the word &#8220;<a title="Reid Cycles Forum Review" href="http://forums.whirlpool.net.au/archive/1633997" target="_blank">forums</a>&#8221; and &#8220;<a title="Google Search for Reid Cycles" href="http://www.google.com.au/search?client=safari&rls=en&q=reid+bike+reviews&ie=UTF-8&oe=UTF-8&redir_esc=&ei=HLJBUazYCMiKmwWR74DQAg" target="_blank">reviews</a>&#8221; looking for owner feedback and more important how the company dealt with any negative feedback.</li>
<li>Additional research for video reviews of the <a title="Youtube product review" href="http://www.youtube.com/watch?v=1wExJKPkfxI" target="_blank">product on YouTube</a>.</li>
<li>Using <a title="Advanced Search" href="https://twitter.com/search?q=reid%20cycles&src=typd" target="_blank">twitter advanced search</a> to track down other owners and <a href="https://twitter.com/jnowland/status/271516001483497472" target="_blank">asking for non bias reviews</a>.</li>
<li>Present the above research to my wife…</li>
<li>Going in-store, armed to the teeth with information.</li>
</ol>
<p>In this instance I wanted a new entry level road bike that could enable me to ride greater distances than I could on a mountain bike and keep up with my mates on their road bikes.</p>
<p>I ended up settling on a <a title="2013 Reid Osprey" href="http://www.reidcycles.com.au/bicycles/road-bikes/2013-reid-osprey-road-bike.html" target="_blank">2013 Reid Osprey Road Bike</a> from the information I could find there was little difference between it and other entry level bikes from other brands that were at-least another $400 extra.</p>
<h3>Controlling and influencing these channels.</h3>
<p>Reid Cycles did a good job with their digital marketing and have control and influence over all the research channels I went down:</p>
<p>They were on eBay (with a decent rating).<br />
The used <a href="http://www.google.com.au/ads/innovations/remarketing.html">display advertising re-marketing</a> after I went to their site so their ads were everywhere.<br />
They used Adsense on other bike searches.<br />
They had a suspicious amount of single post forum users leaving positive reviews.<em> This was actually a turn off.<br />
</em>They were on social media.</p>
<h3>Gorilla Digital Marketing done right:</h3>
<p>For a company that appears to do majority of it&#8217;s business online they seem to recognise the importance of getting their product in-front of consumers (through eBay and SEO) that don&#8217;t know their brand and tell their story through a rather controlled way over a series of third party sites. This is a classic example of a smaller company using Gorilla Marketing tactics with far less overheads of the larger companies.</p>
<p>While in the case of Reid Cycles I&#8217;m unsure if  a third party company is behind all of their digital marketing but it is good to see people executing on their digital strategy and providing a decent product to go with it.</p>
<p>So to hopefully help others in my shoes…</p>
<h3><strong>My 2013 Reid Osprey Review:</strong></h3>
<p>I&#8217;ve done around 450km on the bike so far and haven&#8217;t had any issues other than a flat. Which you will get as the tires are pretty average and thin. The $100 free kit they offer with every bike is pure marketing spin with the actual kit being low quality. I wasn&#8217;t able to even get my tires over 80 PSI with the pump without the plastic bending and looking like I would snap it. To be honest I wasn&#8217;t overly surprised by this and it certainly didn&#8217;t influence my decision but the kit still didn&#8217;t meet my low expectations I had for it.</p>
<p>The bike does it&#8217;s job and has allowed me to achieve the things I wanted from it so far, I can do 2hr plus rides on it and keep up with my mates on far more expansive bikes. The gears have ran finIt should be noted this is from the perceptive of a first time rider who would be no where near as fussy as others.</p>
<p>Now phase two of this plan is to ride this new road bike enough to convince my wife I need (and will used) a bike with carbon everything and mental telepathy gears.</p>

