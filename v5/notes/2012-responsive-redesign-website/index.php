            <h3>Story of my 2012 redesign website</h3>
            <span class="textDate">23 <em>of</em> Apr, 2012 </span>


            <p><div class="swpf-img"><img class="alignnone size-full wp-image-40" title="Responsive Images" src="../wp-content/uploads/2012/04/responsiveWebsiteRedesign.jpg" alt="" width="904" height="602" /></div></p>
<p>I commenced designing the current version of this site in mid 2010, starting with the re-design of <a title="The Logo Rebrand (2012)" href="../logo-redesign/">my personal branding</a>.  It was around this time the expression &#8220;Responsive Web Design&#8221; (or <a title="RWD" href="http://twitter.com/#!/search/%23rwd">#RWD</a> on twitter) was becoming popular with bloggers and on twitter.  Unsure whether Responsive Web Design was  just another web trend or something I should take note of I invested in a <a title="Responsive Web Design" href="http://www.abookapart.com/products/responsive-web-design">great book written by Ethan Marcotte</a>.  This book bridged the gap in my understanding, and provided a comprehensive overview of what RWD was and its application with modern devices.  Most importantly the book left me with the understanding that RWD wasn&#8217;t just an alternate approach to mobile designs but a solution to a new problem started by Steve Jobs with the launch of &#8220;post desktop devices.&#8221;</p>
<p>It was a short time later that I discovered the mobile framework &#8220;<a title="Responsive Framework" href="http://stuffandnonsense.co.uk/projects/320andup/">320 and up</a>&#8221; which utilised a &#8220;Mobile First&#8221; approach.  That is where you start with a mobile design and build on the layers to form a desktop version for broadband users.</p>
<p>It was safe to say that with all these new developments and understanding of RWD and mobile frameworks my approach to web design had dramatically shifted.</p>
<h3>Goals</h3>
<ol>
<li>Refresh my site with new branding</li>
<li>Change the focus of my site from portfolio based to content based.</li>
<li>Apply my new understanding and learn more about responsive web design and designing for mobile.</li>
</ol>
<h3>Process</h3>
<h4>Step 1 &#8211; Wire frames</h4>
<p>After a few pen and paper sketches I jumped into Adobe Illustrator and made two art boards — mobile (320px wide) and desktop (1024px wide).</p>
<p><div class="swpf-img"><img class="alignnone size-full wp-image-56" title="Responsive Illustrator Artboards" src="../wp-content/uploads/2012/04/responsiveArtboards.jpg" alt="" width="831" height="561" /></div></p>
<p><em>Note: While some of the design was done in illustrator, a lot of it was done inside the browser.</em></p>
<h4>Step 2 &#8211; Design</h4>
<p>After a few wireframes I was ready to jump in and give the site it&#8217;s look. I&#8217;ve had enough experience to know designing for yourself is painful but fun. There tends to be a horrible mixture of a perfectionist without constraints of time and money. A problem we never encounter on a client job.</p>
<p><strong>Rule of self-branding:</strong></p>
<p><div class="swpf-img"><img class="alignnone size-full wp-image-58" title="Self Branding Rule" src="../wp-content/uploads/2012/04/selfBrandingRule.gif" alt="" width="571" height="180" /></div></p>
<p>I was right &#8211; The design was a long drawn out process with lots of experiments with layout and colour combinations. The project was drawn out longer with my iMac having some colour issues were what I saw looked fine but on other screens looked terrible. Which resulted in myself using the <a href="http://itunes.apple.com/au/app/mypantone/id329515634?mt=8" target="_blank">Pantone iPhone app</a>.</p>
<h4>Step 3 &#8211; Coding</h4>
<p>In conjunction with wanting to refresh my own site, I also wanted to learn everything I could about RWD and avail myself of as much experience I could. Whilst I enjoy experimenting with new technologies, I prefer to self experiment rather than do it at the expense or under the pressure of client demands and expectations..</p>
<p>I used 320 and up for my framework. I read and re-read the commented code in html5boiler plate to ensure I am building according to &#8220;best practises&#8221;.</p>
<p><strong>The final outcome of my website was built using these technologies:</strong></p>
<ol>
<li>Html5</li>
<li>CSS3</li>
<li>JQuery</li>
<li>Php</li>
<li>WordPress</li>
</ol>
<p>One of the more unique features about the site is the <a title="Responsive navigation with noscript fallback" href="../responsive-navigation-with-noscript-fallback/" target="_blank">menu system on the mobile version</a>. The menu uses a jQuery slideout to <strong>maximise screen area</strong> with a &lt;no script&gt; anchor in the footer as a fallback for users without javascript.</p>
<h4>Just launch already</h4>
<p>After enough delays I decided to set this site free. There are a few items I look forward to adding later when the time is right.</p>
<p>Enjoy.</p>
<p>&nbsp;</p>

