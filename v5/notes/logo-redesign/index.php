            <h3>The Logo Rebrand (2012)</h3>
            <span class="textDate">25 <em>of</em> Apr, 2012 </span>


            <p>My original logo was 5 years old. That is roughly 25.347222 x Longer than Kim Kardashian&#8217;s last marriage. Just as it had its attention, scored a few jobs and help earn an income &#8211; so did my logo. After all that time, I also wasn&#8217;t in love with my logo. It was time to move on.</p>
<h4>Facing some facts</h4>
<p>From what I&#8217;ve seen it is rare that a designer won&#8217;t grow out of their own visual identity. I personally believe this is because of either:</p>
<ol>
<li>We get tired of seeing things after a period of time</li>
<li>We improve over time and get embarrassed by our old work/short comings.</li>
</ol>
<p>For similar reasons &#8211; I don&#8217;t have a tattoo.</p>
<p>My original logo was designed whilst completing my diploma in graphic design and served as a means to get my foot in the door with a professional agency. While my original logo design and branding received much positive feedback, over the recent years the ambiguousness of the design has been the subject of some odd comments. In hindsight, the logo itself lacked focus and direction, and the interchangeable use of the the upside &#8220;?&#8221; and&#8221;J&#8221; created much confusion as to its meaning.</p>
<p><div class="swpf-img"><img class="alignnone size-full wp-image-53" title="Webdesigner Logo Evolution" src="../wp-content/uploads/2012/04/webdesignerLogoEvolution.gif" alt="" width="598" height="403" /></div></p>
<h4>The new logo</h4>
<p>For my next branding my aim was to encapsulate what I strive to communicate everyday in my designs.</p>
<h4>Clarity and simplicity.</h4>
<p>With logos I&#8217;m a huge fan of minimalism &#8211; when creating a logo it is important to remember that the logo is just one element of your branding. Logos should be utilised to entice your target audiences, be highly recognisable, and draw association with your product. They are NOT there to tell the full story of your branding.</p>
<h4>Enter the motif approach</h4>
<p><div class="swpf-img"><img class="alignnone size-full wp-image-60" title="Wedding Motif Logo" src="../wp-content/uploads/2012/04/weddingMotif.jpg" alt="" width="450" height="192" /></div></p>
<p><em>Above: Motif branding used for my own wedding — J&amp;A.</em></p>
<p>In dabbling with various motif designs for my own wedding, I began appreciating their use and diversity.  Motifs while often simple in design work well both on small or large scales and can provide important continuity in your branding.</p>
<h4>Making the logo</h4>
<p>As much as I would normally be able to show pages of logo sketches I cant because truth be told&#8230;</p>
<p><em>&lt;overshare&gt;</em></p>
<p>My initial logo redesign was done in my shower, drawing on the frosted glass one morning.  Later that same week it was finalised  in illustrator. It had only taken me 3 years of study, 5 years working professionally and two hours on a winter night for me to finally complete the re-design of my logo.</p>
<p><em>&lt;/overshare&gt;</em></p>
<p>&nbsp;</p>
<p>Would love to hear your thoughts/feedback below.</p>

