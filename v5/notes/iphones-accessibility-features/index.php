            <h3>Learning by using the iPhone&#8217;s accessibility features</h3>
            <span class="textDate">12 <em>of</em> Nov, 2012 </span>


            <p>By digging deeper into accessibility features on my iPhone, I was able to make a good dent in the outstanding articles in my &#8220;read later&#8221; list. This turned the iPhone from a device I don&#8217;t overly enjoy reading from to a rather efficient way to learn on the go.</p>
<p>Recently I have become a father which has made it more difficult to juggle out of office hours learning such as reading industry blogs and listening to podcasts. In the web design field this type of learning is almost compulsory if you want to try and stay on top of the ever changing best practices for web design and online marketing.</p>
<h4>The scenario</h4>
<p>Upon becoming a father I knew that a large amount of lifestyle changes were heading my way so I preemptively culled my twitter feed that is majority industry related and switched from reading lots of blog articles at home to listening to podcasts on my walk in to work. As I do enjoy listening to industry related podcasts to aid my learning but they often don&#8217;t provide the in-depth analyses you usually get from well written articles. Ideally I would of loved to be able to read the articles on my way into work, trying to do so would more than likely have me walking into someone or something. Instead I started diving into a few iPhone settings in the &#8220;accessibility tab&#8221; hoping to find a some type of way to read the webpage I&#8217;m on. After a bit of playing around, I now have enabled &#8220;Speak selection&#8221;.</p>
<p>This allows me to select an entire blog post and &#8220;play&#8221; it which solves my problem, allowing me to listen to the blog post while walking in.</p>
<p>&nbsp;</p>
<p><a href="../wp-content/uploads/2012/11/iphone-text-to-speak.jpg"><div class="swpf-img"><img class="size-medium wp-image-119 alignnone" title="iphone-text-to-speak" src="../wp-content/uploads/2012/11/iphone-text-to-speak-169x300.jpg" alt="" width="169" height="300" /></div></a></p>
<p>While I previously have used JAWS and other accessibility tools to test and develop an understanding of how accessibility impacts my web development work. Using accessibility on my iPhone really opened my eyes up to how accessible the device really is. So much so time poor people without any disabilities can benefit from the feature.</p>
<h3>How to turn Speak Selection on</h3>
<p><a href="../wp-content/uploads/2012/11/iphone-accessibility-speak.jpg"><div class="swpf-img"><img class="alignnone size-large wp-image-118" title="iphone-accessibility-speak" src="../wp-content/uploads/2012/11/iphone-accessibility-speak-1024x454.jpg" alt="" width="728" height="322" /></div></a></p>
<p>General &gt; Accessibility &gt; Speak Selection</p>
<h4>Further benefits to &#8220;Speak selection&#8221;</h4>
<p>An additional benefit to using Speak Selection is that it assists in proofing my writing, as I often overlook the linking words in sentences when I review my own work. I&#8217;m finding having my writing read aloud really helps highlight the areas for improvement.</p>
<p>Give it a go and let us know what you think. With 700,000 apps now on iOS there are likely some other apps that also solve this problem another way so if you have any suggestions it would be great to hear from you.</p>

